#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - function to add two ints
 * @a: first int
 * @b: second int
 * Return: solution
 */

int op_add(int a, int b)
{
	return(a + b);
}

/**
 * op_sub - function to subtract two ints
 * @a: first int
 * @b: second int
 * Return: solution
 */

int op_sub(int a, int b)
{
        return(a - b);
}


/**
 * op_mul - function to multiply two ints
 * @a: first int
 * @b: second int
 * Return: solution
 */

int op_mul(int a, int b)
{
        return(a * b);
}


/**
 * op_div - function to divide two ints
 * @a: first int
 * @b: second int
 * Return: solution
 */

int op_div(int a, int b)
{
        return(a / b);
}


/**
 * op_mod - function to mod two ints
 * @a: first int
 * @b: second int
 * Return: solution
 */


int op_mod(int a, int b)
{
        return(a % b);
}

